#!/usr/bin/env bash

function ansi_none {
  echo -e "\e[0m"
}

function ansi_red {
  echo -e "\e[31m"
}

function ansi_green {
  echo -e "\e[32m"
}

function ansi_yellow {
  echo -e "\e[33m"
}

function ansi_blue {
  echo -e "\e[34m"
}

function ansi_magenta {
  echo -e "\e[35m"
}

function ansi_cyan {
  echo -e "\e[36m"
}

function ansi_white {
  echo -e "\e[37m"
}
